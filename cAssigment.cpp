#include <iostream>

using namespace std;

int main(){

    //assigment
    int a = 20;
    // Assigment Operator
    // Variabel = variabel operator ekspresi
    // a.       =. a.       +       5
    // variabel operator ekspresi
    //  a.      +=.         5

    cout << "niali awal variabel " << a << endl;
    // contoh variabel ditambah 5
    a += 5;
    cout << "ditambah 5 menjadi " << a << endl;

     // contoh variabel dikurangi 5
    a -= 5;
    cout << "dikurangi 5 menjadi " << a << endl;

     // contoh variabel dikali 5
    a *= 5;
    cout << "dikali 5 menjadi " << a << endl;

     // contoh variabel dibagi 5
    a /= 5;
    cout << "dibagi 5 menjadi " << a << endl;



    return 0;


}